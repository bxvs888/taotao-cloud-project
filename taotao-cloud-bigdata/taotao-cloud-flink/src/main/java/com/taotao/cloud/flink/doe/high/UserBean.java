public class UserBean {
    private int uid;
    private String name;
    private long ts;

    public UserBean(int uid, String name, long ts) {
        this.uid = uid;
        this.name = name;
        this.ts = ts;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getTs() {
        return ts;
    }

    public void setTs(long ts) {
        this.ts = ts;
    }
}
